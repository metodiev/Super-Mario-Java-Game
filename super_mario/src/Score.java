public class Score {

    private long palyerScore;
    private long playerCoins;
    private int playerLives;
    private int playerTime;
    private int playerLevel;

    public long getPalyerScore() {
        return palyerScore;
    }

    public void setPalyerScore(long palyerScore) {
        this.palyerScore = palyerScore;
    }

    public long getPlayerCoins() {
        return playerCoins;
    }

    public void setPlayerCoins(long playerCoins) {
        this.playerCoins = playerCoins;
    }

    public int getPlayerLives() {
        return playerLives;
    }

    public void setPlayerLives(int playerLives) {
        this.playerLives = playerLives;
    }

    public int getPlayerTime() {
        return playerTime;
    }

    public void setPlayerTime(int playerTime) {
        this.playerTime = playerTime;
    }

    public int getPlayerLevel() {
        return playerLevel;
    }

    public void setPlayerLevel(int playerLevel) {
        this.playerLevel = playerLevel;
    }
}
